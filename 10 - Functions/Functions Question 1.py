# A function definition is a name that the user gives the function that must be called later to use the function.
# It must be clear and concise so that that the user knows what the function does.

# A function call has the name of the function, as well as the parameters in the call in order to use the function.

# A function parameter is an entry that must be inputted into a function in order to get an output.

# A function return value is the result(s) of the function parameters, also known as the output.